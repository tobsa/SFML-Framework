#ifndef LINE_HPP
#define LINE_HPP

////////////////////////////////////////////////////////////////////////////////
// Header files
////////////////////////////////////////////////////////////////////////////////
#include "Shape.hpp"


namespace sfx
{

////////////////////////////////////////////////////////////////////////////////
// A basic line shape
////////////////////////////////////////////////////////////////////////////////
class Line : public Shape
{
public:

    ////////////////////////////////////////////////////////////////////////////////
    // Create a line 
    ////////////////////////////////////////////////////////////////////////////////
    Line(const sf::Vector2f& position0, const sf::Vector2f& position1, const sf::Color& color = sf::Color::White);
    Line(float x0, float y0, float x1, float y1, const sf::Color& color = sf::Color::White);

    ////////////////////////////////////////////////////////////////////////////////
    // Draw the shape
    ////////////////////////////////////////////////////////////////////////////////
    virtual void draw(sf::RenderTarget& target, sf::RenderStates states) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Check if a single point is inside the shape
    ////////////////////////////////////////////////////////////////////////////////
    virtual bool contains(const sf::Vector2f& position) const;
    virtual bool contains(float x, float y) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Get the position and color of the line
    ////////////////////////////////////////////////////////////////////////////////
    const sf::Vector2f& getPosition(std::size_t index) const;
    const sf::Color& getColor(std::size_t index) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Set the position and color
    ////////////////////////////////////////////////////////////////////////////////
    void setPosition(std::size_t index, const sf::Vector2f& position);
    void setPosition(std::size_t index, float x, float y);
    void setColor(std::size_t index, const sf::Color& color);

    ////////////////////////////////////////////////////////////////////////////////
    // Get the line threshold
    ////////////////////////////////////////////////////////////////////////////////
    float getThreshold() const;

    ////////////////////////////////////////////////////////////////////////////////
    // Set the line threshold
    ////////////////////////////////////////////////////////////////////////////////
    void setThreshold(float threshold);

private:

    ////////////////////////////////////////////////////////////////////////////////
    // Private member data
    ////////////////////////////////////////////////////////////////////////////////
    float m_threshold;
};

} // namespace sfx

#endif