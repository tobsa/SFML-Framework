#ifndef POINT_HPP
#define POINT_HPP

////////////////////////////////////////////////////////////////////////////////
// Header files
////////////////////////////////////////////////////////////////////////////////
#include "Shape.hpp"

namespace sfx
{

class Point : public Shape
{
public:

    ////////////////////////////////////////////////////////////////////////////////
    // Create a point
    ////////////////////////////////////////////////////////////////////////////////
    Point();
    Point(const sf::Vector2f& position, const sf::Color& color = sf::Color::White);
    Point(float x, float y, const sf::Color& color = sf::Color::White);

    ////////////////////////////////////////////////////////////////////////////////
    // Draw the shape
    ////////////////////////////////////////////////////////////////////////////////
    virtual void draw(sf::RenderTarget& target, sf::RenderStates states) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Check if a single point is inside the shape
    ////////////////////////////////////////////////////////////////////////////////
    virtual bool contains(const sf::Vector2f& position) const;
    virtual bool contains(float x, float y) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Set the position of the point
    ////////////////////////////////////////////////////////////////////////////////
    void setPosition(const sf::Vector2f& position);
    void setPosition(float x, float y);
    void setX(float x);
    void setY(float y);

    ////////////////////////////////////////////////////////////////////////////////
    // Set the color of the point
    ////////////////////////////////////////////////////////////////////////////////
    void setColor(const sf::Color& color);

    ////////////////////////////////////////////////////////////////////////////////
    // Get the position of the point
    ////////////////////////////////////////////////////////////////////////////////
    const sf::Vector2f& getPosition() const;

    ////////////////////////////////////////////////////////////////////////////////
    // Get the color of the point
    ////////////////////////////////////////////////////////////////////////////////
    const sf::Color& getColor() const;
};

} // namespace sfx

#endif