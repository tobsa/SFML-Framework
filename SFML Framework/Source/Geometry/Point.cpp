#include "Point.hpp"
#include <SFML/Graphics/RenderTarget.hpp>

namespace sfx
{
  
////////////////////////////////////////////////////////////////////////////////
Point::Point()
{
    m_vertices.addBack(sf::Vertex(sf::Vector2f(), sf::Color::White));
}

////////////////////////////////////////////////////////////////////////////////
Point::Point(const sf::Vector2f& position, const sf::Color& color)
{
    m_vertices.addBack(sf::Vertex(position, color));
}

////////////////////////////////////////////////////////////////////////////////
Point::Point(float x, float y, const sf::Color& color)
{
    m_vertices.addBack(sf::Vertex(sf::Vector2f(x, y), color));
}

////////////////////////////////////////////////////////////////////////////////
void Point::draw(sf::RenderTarget& target, sf::RenderStates states) const
{
    target.draw(&m_vertices[0], m_vertices.getSize(), sf::Points, states);
}

////////////////////////////////////////////////////////////////////////////////
bool Point::contains(const sf::Vector2f& position) const
{
    return true;
}

////////////////////////////////////////////////////////////////////////////////
bool Point::contains(float x, float y) const
{
    return true;
}

////////////////////////////////////////////////////////////////////////////////
void Point::setPosition(const sf::Vector2f& position)
{
    m_vertices[0].position = position;
}

////////////////////////////////////////////////////////////////////////////////
void Point::setPosition(float x, float y)
{
    setPosition(sf::Vector2f(x, y));
}

////////////////////////////////////////////////////////////////////////////////
void Point::setX(float x)
{
    setPosition(x, getPosition().y);
}

////////////////////////////////////////////////////////////////////////////////
void Point::setY(float y)
{
    setPosition(getPosition().x, y);
}

////////////////////////////////////////////////////////////////////////////////
void Point::setColor(const sf::Color& color)
{
    m_vertices[0].color = color;
}

////////////////////////////////////////////////////////////////////////////////
const sf::Vector2f& Point::getPosition() const
{
    return m_vertices.front().position;
}

////////////////////////////////////////////////////////////////////////////////
const sf::Color& Point::getColor() const
{
    return m_vertices.front().color;
}

} // namespace sfx