#ifndef POLYGON_HPP
#define POLYGON_HPP

////////////////////////////////////////////////////////////////////////////////
// Header files
////////////////////////////////////////////////////////////////////////////////
#include "Shape.hpp"

namespace sfx
{

////////////////////////////////////////////////////////////////////////////////
// A polygon shape class
////////////////////////////////////////////////////////////////////////////////
class Polygon : public Shape 
{
public:

    ////////////////////////////////////////////////////////////////////////////////
    // Create a polygon
    ////////////////////////////////////////////////////////////////////////////////
    Polygon(std::size_t size = 0);

    ////////////////////////////////////////////////////////////////////////////////
    // Draw the shape
    ////////////////////////////////////////////////////////////////////////////////
    virtual void draw(sf::RenderTarget& target, sf::RenderStates states) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Check if a single point is inside the shape
    ////////////////////////////////////////////////////////////////////////////////
    virtual bool contains(const sf::Vector2f& position) const;
    virtual bool contains(float x, float y) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Add a point to the polygon
    ////////////////////////////////////////////////////////////////////////////////
    void addPoint(const sf::Vector2f& position, const sf::Color& color = sf::Color::White);
    void addPoint(float x, float y, const sf::Color& color = sf::Color::White);

    ////////////////////////////////////////////////////////////////////////////////
    // Get the size and a single point
    ////////////////////////////////////////////////////////////////////////////////
    std::size_t getSize() const;
    const sf::Vertex& getPoint(std::size_t index) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Get the position and color of a point
    ////////////////////////////////////////////////////////////////////////////////
    const sf::Vector2f& getPosition(std::size_t index) const;
    const sf::Color& getColor(std::size_t index) const;

    ////////////////////////////////////////////////////////////////////////////////
    // Set new position for a single point
    ////////////////////////////////////////////////////////////////////////////////
    void setPosition(std::size_t index, const sf::Vector2f& position);
    void setPosition(std::size_t index, float x, float y);

    ////////////////////////////////////////////////////////////////////////////////
    // Set new color for a single point
    ////////////////////////////////////////////////////////////////////////////////
    void setColor(const sf::Color& color);
    void setColor(std::size_t index, const sf::Color& color);

    ////////////////////////////////////////////////////////////////////////////////
    // Remove a point from the polygon
    ////////////////////////////////////////////////////////////////////////////////
    void remove(std::size_t index);
};


} // namespace sfx

#endif