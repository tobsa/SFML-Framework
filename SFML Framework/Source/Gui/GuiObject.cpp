#include "GuiObject.hpp"

namespace sfx
{

////////////////////////////////////////////////////////////////////////////////
GuiObject::GuiObject(Application& application) : 
    m_application     (application),
    m_hover           (false),
    m_pressed         (false),
    m_enabled         (true)
{
}

////////////////////////////////////////////////////////////////////////////////
GuiObject::~GuiObject()
{
}


////////////////////////////////////////////////////////////////////////////////
void GuiObject::enable()
{
    m_enabled = true;
}

////////////////////////////////////////////////////////////////////////////////
void GuiObject::disable()
{
    m_enabled = false;
}

////////////////////////////////////////////////////////////////////////////////
bool GuiObject::isEnabled() const
{
    return m_enabled;
}


////////////////////////////////////////////////////////////////////////////////
bool GuiObject::isHover() const
{
    return m_hover;
}

////////////////////////////////////////////////////////////////////////////////
bool GuiObject::isPressed() const
{
    return m_pressed;
}

////////////////////////////////////////////////////////////////////////////////
void GuiObject::setHover(bool hover)
{
    m_hover = hover;
}

////////////////////////////////////////////////////////////////////////////////
void GuiObject::setPressed(bool pressed)
{
    m_pressed = pressed;
}

} // namespace sfx