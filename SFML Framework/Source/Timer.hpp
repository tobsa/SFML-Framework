#ifndef TIMER_HPP
#define TIMER_HPP

////////////////////////////////////////////////////////////////////////////////
// Header files
////////////////////////////////////////////////////////////////////////////////
#include <SFML/System/Clock.hpp>

namespace sfx
{

////////////////////////////////////////////////////////////////////////////////
// A simple pausable clock
////////////////////////////////////////////////////////////////////////////////
class Timer 
{
public:

    ////////////////////////////////////////////////////////////////////////////////
    // Create a timer
    ////////////////////////////////////////////////////////////////////////////////
    Timer(bool paused = false);

    ////////////////////////////////////////////////////////////////////////////////
    // Get the elapsed time since the timer was created (or restarted)
    ////////////////////////////////////////////////////////////////////////////////
    sf::Time getElapsedTime() const;

    ////////////////////////////////////////////////////////////////////////////////
    // Restart the timer
    ////////////////////////////////////////////////////////////////////////////////
    sf::Time restart(bool paused = false);

    ////////////////////////////////////////////////////////////////////////////////
    // Pause and resume the timer
    ////////////////////////////////////////////////////////////////////////////////
    void pause();
    void resume();

    ////////////////////////////////////////////////////////////////////////////////
    // Toggle the timer between paused and unpaused
    ////////////////////////////////////////////////////////////////////////////////
    void toggle();

    ////////////////////////////////////////////////////////////////////////////////
    // Is the timer paused?
    ////////////////////////////////////////////////////////////////////////////////
    bool isPaused() const;    
    
private:

    ////////////////////////////////////////////////////////////////////////////////
    // Private member data
    ////////////////////////////////////////////////////////////////////////////////
    sf::Clock m_clock;
    bool      m_paused;
    sf::Time  m_time;
};

} // namespace sfx

#endif